namespace OpenTemple.Core.Ui.Widgets
{
    public struct Margins
    {
        public float Left;
        public float Top;
        public float Right;
        public float Bottom;

        public Margins(float left, float top, float right, float bottom)
        {
            Left = left;
            Top = top;
            Right = right;
            Bottom = bottom;
        }
    }
}