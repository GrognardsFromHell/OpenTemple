    public static class KeyboardKeys
    {
        static KeyboardKeys()
        {
            // Self-test
            var values = Enum.GetValues<KeyboardKey>();
            if (values.Length != KeyIds.Length)
            {
                throw new Exception("KeyNames table is out-of-sync with KeyboardKey enumeration");
            }
            foreach (var keyboardKey in values)
            {
                var nameIndex = (int) keyboardKey;
                if (nameIndex < 0 || nameIndex >= KeyIds.Length)
                {
                    throw new Exception($"KeyboardKey value ${keyboardKey} is out of range for name table");
                }
            }
        }

        public static string GetKeyId(KeyboardKey key) => KeyIds[(int) key];

