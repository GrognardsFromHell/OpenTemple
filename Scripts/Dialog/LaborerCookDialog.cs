namespace Scripts.Dialog
{
    [DialogScript(261)]
    public class LaborerCookDialog : LaborerCook, IDialogScript
    {
        public bool CheckPrecondition(GameObjectBody npc, GameObjectBody pc, int lineNumber, string originalScript)
        {
            switch (lineNumber)
            {
                case 2:
                    Trace.Assert(originalScript == "not npc.has_met( pc )");
                    return !npc.HasMet(pc);
                case 3:
                case 4:
                    Trace.Assert(originalScript == "npc.has_met( pc )");
                    return npc.HasMet(pc);
                case 11:
                    Trace.Assert(originalScript == "is_daytime()");
                    return Utilities.is_daytime();
                case 12:
                    Trace.Assert(originalScript == "not is_daytime()");
                    return !Utilities.is_daytime();
                default:
                    Trace.Assert(originalScript == null);
                    return true;
            }
        }
        public void ApplySideEffect(GameObjectBody npc, GameObjectBody pc, int lineNumber, string originalScript)
        {
            switch (lineNumber)
            {
                case 70:
                    Trace.Assert(originalScript == "game.global_flags[65] = 1");
                    SetGlobalFlag(65, true);
                    break;
                default:
                    Trace.Assert(originalScript == null);
                    return;
            }
        }
        public bool TryGetSkillCheck(int lineNumber, out DialogSkillChecks skillChecks)
        {
            switch (lineNumber)
            {
                default:
                    skillChecks = default;
                    return false;
            }
        }
    }
}
