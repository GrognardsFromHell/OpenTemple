        internal static bool TryMapSpellId(ExpressionSyntax right, out ExpressionSyntax spellNameRef)
        {
            if (!right.IsKind(SyntaxKind.NumericLiteralExpression))
            {
                spellNameRef = null;
                return false;
            }
            var spellId = (int)((LiteralExpressionSyntax)right).Token.Value;
            spellNameRef = MemberAccessExpression(
                                    SyntaxKind.SimpleMemberAccessExpression,
                                    IdentifierName("WellKnownSpells"),
                                    IdentifierName(SpellNames[spellId])
                                    );
            return true;
        }
